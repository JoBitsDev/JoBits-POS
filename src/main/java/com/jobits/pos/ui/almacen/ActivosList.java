    @Override
    public void updateView() {
        modelLista = new RestManagerAbstractTableModel<ActivoFijo>(getController().getItems(), jTable1) {
            @Override
            public int getColumnCount() {
                return 7;
            }

            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {
                switch (columnIndex) {
                    case 0:
                        return items.get(rowIndex).getNumeroActivo();
                    case 1:
                        return items.get(rowIndex).getGrupo();
                    case 2:
                        return items.get(rowIndex).getUbicacion();
                    case 3:
                        return items.get(rowIndex).getDescripcion();
                    case 4:
                        return items.get(rowIndex).getTipoActivo();
                    case 5:
                        return items.get(rowIndex).getEstadoActivo();
                    case 6:
                        return items.get(rowIndex).getValorActual();
                    default:
                        return null;
                }
            }

            @Override
            public String getColumnName(int column) {
                switch (column) {
                    case 0:
                        return "Inventario";
                    case 1:
                        return "Grupo";
                    case 2:
                        return "Ubicacion";
                    case 3:
                        return "Descripcion";
                    case 4:
                        return "Tipo";
                    case 5:
                        return "Estado";
                    case 6:
                        return "Valor";
                    default:
                        return null;
                }
            }
        };
        jTable1.setModel(modelLista);
        jLabelTotal.setText("Valor Total :   "+ utils.calcularSumaTabla(jTable1, 6));
    }

