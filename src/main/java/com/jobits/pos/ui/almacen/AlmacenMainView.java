    @Override
    public void uiInit() {
        initComponents();

        AddFromPanel.AddFromPanelBuilder<InsumoAlmacen, Insumo> builder = AddFromPanel.builder();

        builder.addAction(getPresenter().getOperation(ACTION_AGREGAR_INSUMO));
        builder.removeAction(getPresenter().getOperation(ACTION_ELIMINAR_INSUMO));
        builder.autoCompletitionData(getPresenter().getModel(PROP_LISTA_INSUMOS_DISPONIBLES));
        builder.autoCOmpletitionDataSelection(getPresenter().getModel(PROP_INSUMO_DISPONIBLE_SELECCIONADO));
        builder.jTextFieldDataName("Insumos");
        builder.tableDataHolder(getPresenter().getModel(PROP_LISTA_INSUMOS_CONTENIDOS));
        builder.tableSelectionDataHolder(getPresenter().getModel(PROP_INSUMO_CONTENIDO_SELECCIONADO));

        BindableTableModel<InsumoAlmacen> tableModel
                = new BindableTableModel<InsumoAlmacen>() {
            @Override
            public int getColumnCount() {
                return 6;
            }

            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {
                switch (columnIndex) {
                    case 0:
                        return (String) getRow(rowIndex).getInsumo().getCodInsumo();
                    case 2:
                        return getRow(rowIndex).getInsumo().getNombre();
                    case 1:
                        return getRow(rowIndex).getInsumo().getUm();
                    case 3:
                        return utils.setDosLugaresDecimalesFloat(getRow(rowIndex).getCantidad());
                    case 4:
                        return getRow(rowIndex).getCantidad() != 0
                                ? utils.setDosLugaresDecimalesFloat(getRow(rowIndex).getValorMonetario() / getRow(rowIndex).getCantidad()) : utils.setDosLugaresDecimalesFloat(0);
                    case 5:
                        return utils.setDosLugaresDecimalesFloat(getRow(rowIndex).getValorMonetario());
                    default:
                        return null;
                }
            }

            @Override
            public String getColumnName(int column) {
                switch (column) {
                    case 0:
                        return "Codigo";
                    case 2:
                        return "Nombre";
                    case 1:
                        return "UM";
                    case 3:
                        return "En Almacen";
                    case 4:
                        return "Costo Unitario Prom (" + R.COIN_SUFFIX + ")";
                    case 5:
                        return "Valor Total (" + R.COIN_SUFFIX + ")";
                    default:
                        return null;
                }
            }

            @Override
            public Class<?> getColumnClass(int columnIndex) {
                switch (columnIndex) {
                    case 3:
                        return Float.class;
                    case 4:
                        return Float.class;
                    case 5:
                        return Float.class;
                    default:
                        return super.getColumnClass(columnIndex);
                }
            }

        };
        builder.tableModel(tableModel);

        tableInsumos = builder.build();
        tableInsumos.getjButtonAgregarProd().setText("Registrar");
        tableInsumos.getjTableCrossReference().getColumnModel().getColumn(3).setCellRenderer(utils.numberColumCellRender());
        tableInsumos.getjTableCrossReference().getColumnModel().getColumn(4).setCellRenderer(utils.numberColumCellRender());
        tableInsumos.getjTableCrossReference().getColumnModel().getColumn(5).setCellRenderer(utils.numberColumCellRender());
        jPanelTabla.add(tableInsumos, BorderLayout.CENTER);
    }

    @Override

    public String getViewName() {
        return VIEW_NAME;
    }

