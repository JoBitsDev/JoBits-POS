    @Override
    public BindableTableModel<Categoria> generateTableModel() {
        return new BindableTableModel<Categoria>(jTableList) {
            @Override
            public int getColumnCount() {
                return 3;
            }

            @Override
            public Object getValueAt(int rowIndex, int columnIndex) {
                switch (columnIndex) {
                    case 0:
                        return getRow(rowIndex).getIdcategoria();
                    case 1:
                        return getRow(rowIndex).getNombre();
                    case 2:
                        return getRow(rowIndex).getColor();
                    default:
                        return null;
                }
            }

            @Override
            public String getColumnName(int column) {
                switch (column) {
                    case 0:
                        return "Codigo";
                    case 1:
                        return "Nombre";
                    case 2:
                        return "Color";
                    default:
                        return null;
                }
            }

            @Override
            public Class<?> getColumnClass(int columnIndex) {
                switch (columnIndex) {
                    case 0:
                        return int.class;
                    case 1:
                        return String.class;
                    case 2:
                        return Color.class;
                    default:
                        return super.getColumnClass(columnIndex); //To change body of generated methods, choose Tools | Templates.
                }
            }
        };
    }

