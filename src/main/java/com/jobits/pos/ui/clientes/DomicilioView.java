    private void jTextFieldPrecioEnvioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPrecioEnvioKeyTyped
        char c = evt.getKeyChar();
        if (((c < '0') || (c > '9')) && (c != '\b') && (c != '.')) {
            evt.consume();
        }
        if ((c == '.') && (jTextFieldPrecioEnvio.getText().contains("."))) {
            evt.consume();
        }
        if ((c == '0') && (jTextFieldPrecioEnvio.getText().isEmpty())) {
            evt.consume();
        }
    }//GEN-LAST:event_jTextFieldPrecioEnvioKeyTyped

