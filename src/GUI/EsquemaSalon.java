    private void updateMesasTable() {
        mesas = staticContent.mesasJPA.findMesaEntities();
        
        ArrayList [] rowdata = comun.initArray(new ArrayList[4]);
        
        
        for (Mesa mesa : mesas) {
            rowdata[0].add(mesa.getCodMesa());
            rowdata[1].add(mesa.getEstado());
            rowdata[2].add(mesa.getCapacidadMax());
            rowdata[3].add(mesa.getAreacodArea().getCodArea());
        }
        try {
            comun.UpdateTable(rowdata, jXTableListadoMesas);
        } catch (Exception ex) {
            Logger.getLogger(EsquemaSalon.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    private void updateAreasTable() {
         areas = staticContent.areaJPA.findAreaEntities();
        comboBoxCodigoArea.removeAllItems();
         
        ArrayList [] rowdata = comun.initArray(new ArrayList[3]);
        
        
        for (Area a : areas) {
            rowdata[0].add(a.getCodArea());
            rowdata[1].add(a.getNombre());
            rowdata[2].add(a.getCapacidad());
            comboBoxCodigoArea.addItem(a.getCodArea());
        }
        try {
            comun.UpdateTable(rowdata, jXTableListadoAreas);
        } catch (Exception ex) {
            Logger.getLogger(EsquemaSalon.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
    

