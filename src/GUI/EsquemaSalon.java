    private void buttonAgregarAreaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAgregarAreaActionPerformed
        
        String nombre = textFieldNombre.getText();//TODO: faltan excepciones aqui
        int cap = (int) jSpinnerArea.getValue();
                
        if(this.jXLabelCodArea.getText().contains("A-")){
          Area a = staticContent.areaJPA.findArea(jXLabelCodArea.getText());
          a.setNombre(nombre);
          a.setCapacidad(cap);
            try {
                staticContent.areaJPA.edit(a);
            } catch (Exception ex) {
                Logger.getLogger(EsquemaSalon.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        else{
             Area a = new Area("A-"+ areas.size());
          a.setNombre(nombre);
          a.setCapacidad(cap);
            try {
                staticContent.areaJPA.create(a);
            } catch (Exception ex) {
                Logger.getLogger(EsquemaSalon.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            
        }
        
        updateAreasTable();
        cleanPanel();
        
        
    }//GEN-LAST:event_buttonAgregarAreaActionPerformed

