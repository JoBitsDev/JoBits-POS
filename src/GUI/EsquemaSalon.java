    private void buttonEliminarMesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEliminarMesaActionPerformed
       try {
            if (jXTableListadoMesas.getSelectedRow() != -1) {
                String cod = (String) jXTableListadoMesas.getValueAt(jXTableListadoMesas.getSelectedRow(), 0);
                if (JOptionPane.showConfirmDialog(this, "Seguro desea eliminar la mesa " + cod ) == JOptionPane.YES_OPTION) {
                    staticContent.mesasJPA.destroy(cod);
                    
                    updateMesasTable();
                }
                
                
                
            } else {
                JOptionPane.showMessageDialog(this, "No se ha seleccionado ningun elemento.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar el puesto.");
        }
    }//GEN-LAST:event_buttonEliminarMesaActionPerformed

