    private void llenarTabla() {

        DefaultTableModel model = (DefaultTableModel) jTableListaTrabajadores.getModel();
       

        if (jTableListaTrabajadores.getRowCount() != 0) {
            comun.limpiarTabla(jTableListaTrabajadores);
        }

        for (Personal x : trabajadores) {
            Object[] row = {
                x.getUsuario(),
                x.getDatosPersonales().getNombre(),
                x.getDatosPersonales().getApellidos(),
                x.getPuestoTrabajoList().get(0).getNombrePuesto(),
                x.getDatosPersonales().getTelefonoMovil()
            };
             model.addRow(row);
        
    }
        
    }
    

