    public void updateValorTotal(){
        float total = 0;
        for (int i = 0; i < model.getItems().size(); i++) {
          total +=  (float)model.getValueAt(i, 2);
        }
        jLabelVALORTotal.setText(R.formatoMoneda.format(total));
        getController().setValorTotal(total);
    }
