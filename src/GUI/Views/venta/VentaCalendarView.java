    public void init() {
        updateCalendar();
        jScrollPane1.getViewport().setOpaque(false);

        int month = jMonthChooser1.getMonth();
        int year = jYearChooser1.getYear();
        model = new RestManagerAbstractTableCellModel<Venta>(findVentas(month, year), jTableCalendar) {
            @Override
            public int getColumnCount() {
                return 7;
            }

            @Override
            public Class<?> getColumnClass(int columnIndex) {
                return Venta.class;
            }

            @Override
            public Venta getValueAt(int rowIndex, int columnIndex) {
                int linearPos = rowIndex * getColumnCount() + columnIndex;
                if (linearPos >= monthOffset) {
                    for (Venta x : getItems()) {
                        if ((x.getFecha().getDate() - 1) == (linearPos - monthOffset)) {
                            return x;
                        }
                    }
                }
                return null;
            }

            @Override
            public int getRowCount() {
                return 6;
            }

            @Override
            public String getColumnName(int column) {
                switch (column) {
                    case 0:
                        return "Lunes";
                    case 1:
                        return "Martes";
                    case 2:
                        return "Miercoles";
                    case 3:
                        return "Jueves";
                    case 4:
                        return "Viernes";
                    case 5:
                        return "Sabado";
                    case 6:
                        return "Domingo";
                    default:
                        return null;
                }
            }
        };
        jTableCalendar.setModel(model);
        updatePanelEstaidisticas(model.getItems());
//        model.addSelectionListener((PropertyChangeEvent evt) -> {
//            updatePanelEstaidisticas(getSelectedVentas());
//        });
        jTableCalendar.setDefaultRenderer(Venta.class, new VentaCellRender());
        //jTableCalendar.setRowHeight(80);
    }

    public List<Venta> findVentas(int month, int year) {
        ArrayList<Venta> ret = new ArrayList<>();
        getController().getModel().findAll().stream().map((x) -> {
            if (x.getFecha().getMonth() == month && x.getFecha().getYear() + 1900 == year) {
                ret.add(x);
            }
            return x;
        }).forEachOrdered((Venta _item) -> {
            Collections.sort(ret, (Venta o1, Venta o2) -> o1.getFecha().compareTo(o2.getFecha()));
        });
        return ret;
    }

