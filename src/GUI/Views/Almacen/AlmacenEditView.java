    private class MyTableModel extends restManager.util.RestManagerAbstractTableModel<Insumo> {

        public MyTableModel(List<Insumo> items, JTable table) {
            super(items, table);
        }

        @Override
        public int getColumnCount() {
            return 5;
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            switch (columnIndex) {
                case 0:
                    return items.get(rowIndex).getCodInsumo();
                case 2:
                    return items.get(rowIndex).getNombre();
                case 1:
                    return items.get(rowIndex).getUm();
                case 3:
                    return items.get(rowIndex).getCantidadExistente();
                case 4:
                    return items.get(rowIndex).getCostoPorUnidad();
                case 5:
                    return items.get(rowIndex).getCantidadExistente() * items.get(rowIndex).getCostoPorUnidad();
                default:
                    return null;

            }
        }

        @Override
        public String getColumnName(int column) {
            switch (column) {
                case 0:
                    return "Codigo";
                case 2:
                    return "Nombre";
                case 1:
                    return "UM";
                case 3:
                    return "En Almacen";
                case 4:
                    return "Costo Unitario (Prom)";
                case 5:
                    return "Valor Total";
                default:
                    return null;
            }
        }

        @Override
        public Class<?> getColumnClass(int columnIndex) {
            switch (columnIndex) {
                case 0:
                    return String.class;
                case 1:
                    return String.class;
                case 2:
                    return String.class;
                case 3:
                    return Float.class;
                case 4:
                    return Float.class;
                case 5:
                    return Float.class;
                default:
                    return null;
            }
        }

        @Override
        public boolean isCellEditable(int rowIndex, int columnIndex) {
            return false;
        }

    }

